/* Problema 6: Área y Perímetro de un Círculo */
let radio = 7.5;
let area = Math.PI * Math.pow(radio, 2);
let perimetro = 2 * Math.PI * radio;
console.log(`Radio: ${radio} metros`);
console.log(`Area: ${area.toFixed(2)} metros cuadrados`);
console.log(`Perimetro: ${perimetro.toFixed(2)} metros`);
